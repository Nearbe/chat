#!/bin/bash

# Скрипт для настройки проекта (setup)
# Остановка при любой ошибке
set -e

# Генерация проекта Xcode с помощью XcodeGen
echo "Генерация Xcode проекта..."
xcodegen generate

# Информирование об успешном завершении
echo "Проект успешно сгенерирован!"
echo "Открытие проекта в Xcode..."

# Открытие сгенерированного проекта в Xcode по умолчанию
open Chat.xcodeproj

echo "Готово!"
